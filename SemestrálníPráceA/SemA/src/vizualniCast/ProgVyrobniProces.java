    private void jButtonVlozProcesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVlozProcesActionPerformed
        Iterator it = null;
        it = vp.dejIterator();
        jTextAreaText.setText(null);
        if (jRadioButtonProcesManualni.isSelected()) {
            procesManu = new ProcesManu();
            vp.vlozProces(procesManu, poziceVkladanehoPrvku);

        }
        if (jRadioButtonProcesRoboticky.isSelected()) {
            procesRobo = new ProcesRobo();
            vp.vlozProces(procesRobo, poziceVkladanehoPrvku);
        }
        while (it.hasNext()) {
            IProces pom = (IProces) it.next();
            jTextAreaText.append(pom.toString() + "\n");
        }
    }//GEN-LAST:event_jButtonVlozProcesActionPerformed

