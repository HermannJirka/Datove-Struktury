    private void jButtonGenerujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerujActionPerformed
        int pocetPrvku = 0;
        int genProc = 0;
        int genCas = 0;
        int genPocetPracovniknu = 0;
        IProcesManu manual = null;
        IProcesRobo robot = null;
        try {
            pocetPrvku = Integer.parseInt(jTextFieldPocetPrvku.getText());
            for (int i = 0; i < pocetPrvku; i++) {
                genProc = (int) ((Math.random() * 2) + 1);
                genCas = (int) ((Math.random() * 20) + 1);
                genPocetPracovniknu = (int) ((Math.random() * 10) + 1);
                if (genProc == 1) {
                    manual = new ProcesManu();
                    vp.vlozProces(manual, enumPozice.PRVNI);
                } else {
                    robot = new ProcesRobo();
                    vp.vlozProces(robot, enumPozice.PRVNI);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Spatne zadane cislo \n" + e.getMessage());
        }
    }//GEN-LAST:event_jButtonGenerujActionPerformed

    private void jButtonVytipujKandidatyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVytipujKandidatyActionPerformed

        int casReorganizace = 0;
        int cislo = 0;
        cislo = jComboBoxTypReorganizace.getSelectedIndex();
        try {


            if (cislo == 0) {
                poziceReorganizace = enumReorg.AGREGACE;
            } else {
                poziceReorganizace = enumReorg.DEKOMPOZICE;
            }
            System.out.println(cislo);
            casReorganizace = Integer.parseInt(jTextFieldCasReorganizace.getText());
            fronta = vp.vytipujKandidatiReorg(casReorganizace, poziceReorganizace);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fronta je prazda prvky nesplnuji kriterium");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButtonVytipujKandidatyActionPerformed

