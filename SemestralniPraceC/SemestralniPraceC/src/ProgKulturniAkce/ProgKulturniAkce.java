    private void jButOdeberAkceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButOdeberAkceActionPerformed
        try {

            int pocetUdalosti = kalendarUdalosti.dejMohutnost();
            udalosti = new Udalost[pocetUdalosti];
            udalosti = kalendarUdalosti.dejAkce();
            jTxtAUtridena.setText("");
            jTxtAUtridena.append("===Odebrane Akce===\n");
            for (int i = 0; i < udalosti.length; i++) {
                jTxtAUtridena.append(udalosti[i].toString());
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Ve fronte nejsou zadne udalosti", "Chyba", WIDTH);
        }


    }//GEN-LAST:event_jButOdeberAkceActionPerformed

