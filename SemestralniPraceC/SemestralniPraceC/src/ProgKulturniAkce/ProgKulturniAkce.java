    private void jButReorganizujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButReorganizujActionPerformed
        if (udalostZpristupnena != null) {
            jTxtFNazevAkce.setText(udalostZpristupnena.getNazevUdalosti());
            jTxtFNazevMesta.setText(udalostZpristupnena.getMistoKonani());
            int mesic = Integer.parseInt(jTxtFMesic.getText());
            int den = Integer.parseInt(jTxtFDen.getText());
            int rok = 2012;
            Calendar datum = new GregorianCalendar(rok, mesic, den);
            kalendarUdalosti.preplanujAkci(datum);
        } else {
            JOptionPane.showMessageDialog(this, "Neni zpristupnena udalost k reorganizaci", "Chyba", WIDTH);
        }
    }//GEN-LAST:event_jButReorganizujActionPerformed

